int n;
unsigned long s=0;
///----------------------------------------------
int tinh(){
      for(int i = 1; i <= n; i ++) s += (i & 1 ? i : 0);
      return s;
}
///----------------------------------------------
int main(){
      printf("Nhap mot so nguyen n: ");
      scanf("%d", &n);
      printf("S = 1 + 3 + 5 + ... + n = %ld\n", tinh());
}
